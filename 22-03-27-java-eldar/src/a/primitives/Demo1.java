package a.primitives;

public class Demo1 {

	public static void main(String[] args) {

		// numerals - integers
		byte a = 10;
		short b = 100;
		int c = 1_000;
		long d = 10_000_000_000L;
		// numerals - decimal
		float x = 5.3F;
		double y = 10.658;
		double z = 10D;
		System.out.println(3.333333333333333333333333333333D);
		System.out.println(3.333333333333333333333333333333F);
		// logical
		boolean b1 = true;
		boolean b2 = false;
		boolean b3 = 10 > 100;
		System.out.println(b3);
		// character
		char c1 = 'A';
		char c2 = ' ';
		char c3 = 'B';
		char c4 = '\n';
		char c5 = '8';
		
		System.out.print(c1);
		System.out.print(c2);
		System.out.print(c3);
		System.out.print(c4);
		System.out.print(c5);

	}

}
